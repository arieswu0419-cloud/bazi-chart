// 81 數尾數→五行（三才用）：1,2木・3,4火・5,6土・7,8金・9,0水
function xmNumWx(n) {
  const d = n % 10;
  return d === 1 || d === 2 ? "木" : d === 3 || d === 4 ? "火" :
    d === 5 || d === 6 ? "土" : d === 7 || d === 8 ? "金" : "水";
}
const XM_WX_SHENG = { 木: "火", 火: "土", 土: "金", 金: "水", 水: "木" };
const XM_WX_KE = { 木: "土", 土: "水", 水: "火", 火: "金", 金: "木" };

// 三才吉凶：以「天生人／人生地為順生大吉、比和次吉、相剋為凶」綜合天→人、人→地兩組關係評定。
function xmSancaiTone(tianWx, renWx, diWx) {
  const rel = (a, b) => a === b ? 0 : (XM_WX_SHENG[a] === b ? 1 : (XM_WX_SHENG[b] === a ? 2 : (XM_WX_KE[a] === b ? -1 : -2)));
  // 天→人、人→地：+1 生下、+2 被上生、0 比和、-1 剋下、-2 被剋
  const r1 = rel(tianWx, renWx), r2 = rel(renWx, diWx);
  const score = (v) => v === 1 || v === 2 ? 2 : v === 0 ? 1 : v === -2 ? -2 : -1;
  const s = score(r1) + score(r2);
  return s >= 3 ? "good" : s <= -2 ? "bad" : "mixed";
}

